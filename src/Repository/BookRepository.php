    {
        parent::__construct($registry, Book::class);
    }

    /**
     * @param int $page
     * @param int $limit
     * @return Paginator
     */
    public function paginate($page = 1, $limit = 5): Paginator
    {
        $query = $this->createQueryBuilder('p')
            ->orderBy('p.id', 'DESC')
            ->getQuery();


        $paginator = new Paginator($query);

        $paginator->getQuery()
            ->setFirstResult($limit * ($page - 1))
            ->setMaxResults($limit);

        return $paginator;
    }

